    private void p_searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_p_searchKeyReleased
        // TODO add your handling code here:
           String query = "Select * from product where product_name like concat('%',?,'%')";
        try{
            DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
            dt.setRowCount(0);
            PreparedStatement pst = db.myconn().prepareStatement(query);
            pst.setString(1,p_search.getText());
            ResultSet rs = pst.executeQuery();
            while(rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString(1));
                v.add(rs.getString(2));
                v.add(rs.getString(3));
                 v.add(rs.getString(4));
                  v.add(rs.getDouble(5));
                v.add(rs.getInt(6));  
                 v.add(rs.getInt(7));
                dt.addRow(v);
            }       
        }catch(Exception e) {
                  System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_p_searchKeyReleased


