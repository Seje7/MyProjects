    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String search = p_search_tbl.getText();
        int search_int = Integer.parseInt(search);
        
        String query = "select * from product where pid = ?";
        try{
            PreparedStatement pst = db.myconn().prepareStatement(query);
            pst.setInt(1,search_int);
            ResultSet rs = pst.executeQuery();
            if(rs.next()) {
                p_name.setText(rs.getString("product_name"));
                p_barcode.setText(rs.getString("bar_code"));
                p_price.setText(Double.toString(rs.getDouble("price")));
                p_qty.setText(Integer.toString(rs.getInt("qty")));
                p_cat.setSelectedItem(rs.getString("category"));
                p_sid.setText(Integer.toString(rs.getInt("sid")));
            }
        }catch (SQLException se) {
            se.printStackTrace();
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

